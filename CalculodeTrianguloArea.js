// Solicitar al usuario que ingrese la base del triángulo
var base = parseFloat(prompt("Ingrese la base del triángulo:"));

// Solicitar al usuario que ingrese la altura del triángulo
var altura = parseFloat(prompt("Ingrese la altura del triángulo:"));

// Calcular el área del triángulo (área = 0.5 * base * altura)
var area = 0.5 * base * altura;

// Mostrar el resultado
console.log("El área del triángulo es: " + area);
